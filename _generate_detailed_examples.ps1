Get-ChildItem -Path $base -Recurse -Filter *.md | ForEach-Object {
    $path = $_.FullName
    Write-Host "Processing: $path"
    $raw = Get-Content -Raw -Encoding UTF8 -LiteralPath $path

    $titleMatch = [regex]::Match($raw, '(?m)^#\s+(.+)$')
    if ($titleMatch.Success) { $title = $titleMatch.Groups[1].Value } else { $title = [IO.Path]::GetFileNameWithoutExtension($path) }

    $newExamples = Generate-Examples -title $title

    # Replace existing ## 例题... 到 下一个二级标题（## 练习题 或 ## 自检清单 或 ## 关联讲义）
    $pattern = '## 例题[\s\S]*?(?=(?:## 练习题|## 自检清单|## 关联讲义|$))'
    if ([regex]::IsMatch($raw, $pattern)) {
        $updated = [regex]::Replace($raw, $pattern, $newExamples)
        Set-Content -LiteralPath $path -Value $updated -Encoding UTF8
        Write-Host "Updated: $path"
    } else {
        # 如果没找到例题标题，则在本章学习目标后插入
        $insertAfter = '## 本章学习目标'
        if ($raw -match $insertAfter) {
            $updated = $raw -replace "(## 本章学习目标[\s\S]*?)(\r?\n## )", "`$1`n`n" + $newExamples + "`n`n## "
            Set-Content -LiteralPath $path -Value $updated -Encoding UTF8
            Write-Host "Inserted examples into: $path"
        } else {
            Write-Host "Skipped (no anchor): $path"
        }
    }
}

Write-Output 'Generate script finished.'
